    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        if($request->get("video_id")){
            $materials = Material::where('video_id', $request['video_id'])->get();
            $video = $request->has("video_id")
                ? Video::find($request->get("video_id"))
                : null;
            return view("instructor.materials.create",compact("video", 'materials'));
        }
        else{
            return redirect()->back();
        }
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
           'title' => 'required',
           'video_id' => 'required',
           'file' => 'required|file|max:20000'
        ]);
        $file = Material::add($request->all());
        $file->uploadFile($request['file'], 'file');
        return redirect()->back();
    }

